# 102 浮點數格式化輸出
# 說明:
# 請撰寫一程式，輸入四個分別含有小數1到4位的浮點數，然後將這四個浮點數以欄寬為7、欄與欄間隔一個空白字元、
# 每列印兩個的方式，先列印向右靠齊，再列印向左靠齊，左右皆以直線 |（Vertical bar）作為邊界。
#
# 輸出浮點數到小數點後第二位。

n1 = eval(input())
n2 = eval(input())
n3 = eval(input())
n4 = eval(input())

print(f"|{n1:>7.2f} {n2:>7.2f}|")
print(f"|{n3:>7.2f} {n4:>7.2f}|")
print(f"|{n1:<7.2f} {n2:<7.2f}|")
print(f"|{n3:<7.2f} {n4:<7.2f}|")
