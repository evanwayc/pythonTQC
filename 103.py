# 103 字串格式化輸出
# 說明:
# 請撰寫一程式，輸入四個單字，然後將這四個單字以欄寬為10、欄與欄間隔一個空白字元、每列印兩個的方式，先列印向右靠齊，再列印向左靠齊，左右皆以直線 |（Vertical bar）作為邊界。

n1 = str(input())
n2 = str(input())
n3 = str(input())
n4 = str(input())

print(f"|{n1:>10} {n2:>10}|")
print(f"|{n3:>10} {n4:>10}|")
print(f"|{n1:<10} {n2:<10}|")
print(f"|{n3:<10} {n4:<10}|")
