# 206 等級判斷
# 說明:
# 請使用選擇敘述撰寫一程式，根據使用者輸入的分數顯示對應的等級。標準如下表所示：
#
#   分數           等級
#   80~100          A
#   70~79           B
#   60~69           C
#   <=59            F

s = int(input())

if 80 <= s <= 100:
    print("A")
elif 70 <= s <= 79:
    print("B")
elif 60 <= s <= 69:
    print("C")
elif s <= 59:
    print("F")
